package day4.IfElseConditions;

public class Example2 {

	public static void main(String[] args) {
		char ch = 'u';
		if (ch == 'a') {
			System.out.println(" Given char is vovel " + ch);
		} else if (ch == 'e') {
			System.out.println("Given char is vovel " + ch);
		} else if (ch == 'i') {
			System.out.println("Given char is vovel " + ch);
		} else if (ch == 'o') {
			System.out.println("Given char is vovel " + ch);
		} else if (ch == 'u') {
			System.out.println("Given char is vovel " + ch);
		} else {
			System.out.println("Given char is not vovel " + ch);
		}

	}
}

