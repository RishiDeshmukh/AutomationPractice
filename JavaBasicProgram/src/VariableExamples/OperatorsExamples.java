package VariableExamples;

public class OperatorsExamples {

	public static void main(String[] args) {
		int num1=10,num2=10;
		System.out.println(num1==num2);
		System.out.println(num1!=num2);
		System.out.println(num1>num2);
		System.out.println(num1>=num2);
		System.out.println(num1<num2);
		System.out.println(num1<=num2);
		boolean res=(num1==num2);
		System.out.println(res);
	
	}

}
