package ForLoop;

public class HelloWorld5times {

	public static void main(String[] args) {
		for (int i = 1; i <= 5; i++) {
			System.out.println("hellow world");
		}
		for (int z = 1; z <= 5; z++) {
			System.out.println(z);
			
		}
		System.out.println("@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");
		for (int y = 5; y >= 1; y--) {
			System.out.println(y);
		}
	}
}
